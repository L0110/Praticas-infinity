"""
Você está criando um programa para calcular a área de um retângulo. O programa deve solicitar ao usuário que 
forneça a base e a altura do retângulo. Em seguida, o programa deve calcular a área usando a fórmula:
    Área=Base×Altura
"""

print("**** Calculo da área de um retângulo ****")
a = float(input("insira o tamanho da base: "))
